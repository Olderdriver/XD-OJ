# include <stdio.h>
# include <math.h>
int main(void)
{
    int x;
    int sum=0;
    scanf("%d",&x);

    if(x<=100000)
        sum=x*0.1;
    else if(x<=200000)
        sum=100000*0.1+(x-100000)*0.075;
    else if(x<=400000)
        sum=100000*0.1+100000*0.075+(x-200000)*0.05;
    else if(x<=600000)
        sum=100000*0.1+100000*0.075+200000*0.05+(x-400000)*0.03;
    else if(x<=1000000)
        sum=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(x-600000)*0.015;
    else
        sum=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(x-1000000)*0.01;


    printf("%d",sum);
    return 0;
}
